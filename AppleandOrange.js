function countApplesAndOranges(s, t, a, b, apples, oranges) {
    let appleCount = 0;
    let orangeCount = 0;

    for (let d of apples) {
        let landing = a + d;
        if (landing >= s && landing <= t) {
            appleCount++;
        }
    }

    for (let d of oranges) {
        let landing = b + d;
        if (landing >= s && landing <= t) {
            orangeCount++;
        }
    }

    console.log(appleCount);
    console.log(orangeCount);
}
